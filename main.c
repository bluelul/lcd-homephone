void InitLCD(void)
{
    P1DIR |= SDA+SCK+CS+AO+RST;
    P1OUT&=~RST;
    P1OUT&=~CS;
    __delay_cycles(10000);
    P1OUT|=RST;
    lcdWrite (0, SET_BIAS_9);
    lcdWrite (0, SOFTWARE_RESET);
    lcdWrite (0, DISPLAY_ON);
    lcdWrite (0, POWER_CONTROL );
    lcdWrite(0, 0x26); //regulation ratio
    //lcdWrite(0, 0x2F);
    lcdWrite(0, ENTIRE_DISPLAY_OFF); //turn off Entire display (All bit is 1)
    lcdWrite(0,0b11100000);
}
void display(void)
{

    uint8_t col, i;

    for (i  =0; i<8; i++)
    {
        lcdWrite(0, SET_PAGE | pagemap[i]);
        lcdWrite(0, COL_LSB);
        lcdWrite(0, COL_MSB);
        for (col=0;col<128;col++)
        {
            lcdWrite(1,*(buf+col+i*128));
        }

    }
}
void lcdWrite(int display, uint8_t code)
{

    if (display)
    {
        P1OUT|=AO;
    }
    else P1OUT&=~AO;
    shiftOut(code);
}
void shiftOut(uint8_t code)
{
    int i;
    for (i=7;i>=0;i--)
    {
        if (code&(1<<i))
            P1OUT|=SDA;
        else P1OUT&=~SDA;
        P1OUT|=SCK;
        P1OUT&=~SCK;
    }
}
